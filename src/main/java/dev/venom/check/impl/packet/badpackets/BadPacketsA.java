@CheckInfo(name = "BadPackets (A)", category = Category.PACKET)
public final class BadPacketsA extends Check {

    public BadPacketsA(final PlayerData data) {
        super(data);
    }

    @Override
    public void handle(final Packet packet) {
        if (packet.isRotation() && !isExempt(ExemptType.CLIMBABLE)) {
            final float pitch = Math.abs(data.getRotationProcessor().getPitch());

            if (pitch > 90) {
                fail(String.format("pitch=%.2f", pitch));
            }
        }
    }
}