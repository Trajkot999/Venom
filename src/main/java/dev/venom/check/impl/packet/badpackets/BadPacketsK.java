@CheckInfo(name = "BadPackets (K)", category = Category.PACKET)
public final class BadPacketsK extends Check {

    public BadPacketsK(final PlayerData data) {
        super(data);
    }

    @Override
    public void handle(final Packet packet) {
        if (packet.isUseEntity()) {
            final WrappedPacketInUseEntity wrapper = new WrappedPacketInUseEntity(packet.getRawPacket());

            if (data.getPlayer().getEntityId() == wrapper.getEntityId()) fail("id: " + wrapper.getEntityId());
        }
    }
}